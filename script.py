import pyttsx3
import speech_recognition as sr
import webbrowser

ia = pyttsx3.init()
ia.say('Olá Senhor, O que você deseja buscar?')
ia.setProperty('voice', b'brazil')
ia.setProperty('rate', 100)
ia.setProperty('volume', 1)
ia.runAndWait()

reconhecimento = sr.Recognizer()
